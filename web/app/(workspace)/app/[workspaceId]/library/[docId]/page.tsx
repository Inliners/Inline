'use client'

import { useEffect, useRef, useState } from 'react'
import { useParams, useRouter } from 'next/navigation'
import {
  Loader2, ChevronRight, ArrowLeft, ExternalLink,
  Sparkles, Share2,
} from 'lucide-react'
import FolderDocumentEditor from '@/components/documents/FolderDocumentEditor'
import { getWorkspaceName } from '@/lib/workspaces'
import { loadServerDocument, saveServerDocument, type ServerDocument } from '@/lib/library-api'

/**
 * Server-backed library document viewer/editor.
 *
 * Unlike /app/[workspaceId]/doc/[docId] (localStorage), this route reads and
 * writes to public.documents via /api/library/:docId. It also shows an
 * "Auto-regenerating" badge for auto-generated page recaps and polls every
 * 30 seconds so the UI stays fresh while the recap API runs in the background.
 */
export default function LibraryDocPage() {
  const { workspaceId, docId } = useParams<{ workspaceId: string; docId: string }>()
  const router = useRouter()

  const [doc,     setDoc]     = useState<ServerDocument | null>(null)
  const [title,   setTitle]   = useState('')
  const [loading, setLoading] = useState(true)
  const [saved,   setSaved]   = useState(false)
  const skipNextContentRef = useRef(false)

  useEffect(() => {
    let cancelled = false
    async function load() {
      const d = await loadServerDocument(docId)
      if (cancelled) return
      if (!d) { router.replace(`/app/${workspaceId}/dashboard`); return }
      setDoc(d); setTitle(d.title); setLoading(false)
    }
    void load()
    return () => { cancelled = true }
  }, [docId, workspaceId, router])

  useEffect(() => {
    if (!doc?.autoGenerated) return
    const iv = setInterval(async () => {
      const fresh = await loadServerDocument(docId)
      if (!fresh) return
      if (fresh.updatedAt !== doc.updatedAt) {
        skipNextContentRef.current = true
        setDoc(fresh)
        setTitle(fresh.title)
      } else {
        setDoc(fresh)
      }
    }, 30_000)
    return () => clearInterval(iv)
  }, [docId, doc?.autoGenerated, doc?.updatedAt])

  async function persist(nextTitle: string, nextContent: string) {
    if (!doc) return
    const saved = await saveServerDocument(doc.id, {
      title: nextTitle.trim() || 'Untitled',
      content: nextContent,
    })
    if (saved) {
      setDoc(saved)
      setSaved(true); setTimeout(() => setSaved(false), 1400)
    }
  }

  function handleContentChange(html: string) {
    if (skipNextContentRef.current) {
      skipNextContentRef.current = false
      return
    }
    void persist(title, html)
  }
  function handleTitleBlur() { void persist(title, doc?.content ?? '') }

  if (loading) return (
    <div className="flex items-center justify-center h-40">
      <Loader2 className="w-5 h-5 animate-spin text-muted-foreground" />
    </div>
  )
  if (!doc) return null

  const workspaceName = getWorkspaceName(workspaceId)
  const displayTitle  = title.trim() || 'Untitled'

  return (
    <div className="min-h-full bg-background">
      <div className="fixed top-3 right-4 flex items-center gap-1.5 z-30">
        {doc.autoGenerated && (
          <span
            className="inline-flex items-center gap-1.5 px-2.5 h-7 rounded-lg text-[11px] font-medium text-emerald-600 bg-emerald-50 border border-emerald-100"
            title="This document is regenerated automatically from your captures on the source page."
          >
            <Sparkles className="w-3 h-3" />
            {doc.recapStale ? 'Regenerating…' : 'Auto-regenerating'}
          </span>
        )}
        <span
          className={
            'text-[11px] text-slate-400 transition-opacity duration-300 mr-1 ' +
            (saved ? 'opacity-100' : 'opacity-0')
          }
        >
          Saved
        </span>
        <button
          type="button"
          className="h-7 px-3 rounded-lg text-xs font-medium text-slate-500 border border-slate-200 hover:border-slate-300 hover:text-slate-900 transition-colors cursor-pointer flex items-center gap-1.5"
          onClick={() => { void navigator.clipboard.writeText(window.location.href) }}
          title="Copy link"
        >
          <Share2 className="w-3 h-3" />
          Share
        </button>
      </div>

      <div className="overflow-y-auto scrollbar-minimal">
        <div className="max-w-3xl mx-auto pt-14 pb-40" style={{ paddingLeft: 80, paddingRight: 80 }}>
          <div className="flex items-center gap-2 mb-2">
            <span className="text-sm font-medium text-slate-400">{workspaceName}</span>
            <ChevronRight className="w-3.5 h-3.5 text-slate-300 shrink-0" />
            <span className="text-sm font-medium text-slate-400">Library</span>
            <ChevronRight className="w-3.5 h-3.5 text-slate-300 shrink-0" />
            <span className="text-sm font-medium text-slate-600">{displayTitle}</span>
          </div>

          <input
            value={title}
            onChange={e => setTitle(e.target.value)}
            onBlur={handleTitleBlur}
            className="w-full text-3xl font-semibold text-slate-900 tracking-tight leading-none mb-2 bg-transparent border-none outline-none placeholder:text-slate-200"
            placeholder="Untitled"
          />

          {doc.pageUrl && (
            <a
              href={doc.pageUrl}
              target="_blank"
              rel="noreferrer"
              className="inline-flex items-center gap-1.5 text-xs text-slate-500 hover:text-slate-900 transition-colors mt-1 mb-6"
            >
              <ExternalLink className="w-3 h-3" />
              Source page
            </a>
          )}

          <button
            type="button"
            onClick={() => router.push(`/app/${workspaceId}/history`)}
            className="inline-flex items-center gap-1.5 text-sm font-medium text-slate-500 hover:text-slate-900 transition-colors mt-2 mb-8 cursor-pointer w-max"
          >
            <ArrowLeft className="w-3.5 h-3.5" />
            Back to history
          </button>

          <FolderDocumentEditor
            key={doc.updatedAt}
            content={doc.content}
            onChange={handleContentChange}
          />
        </div>
      </div>
    </div>
  )
}
